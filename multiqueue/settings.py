def _postgres_from_url(database_url):
    """Convertit une URL PostgreSQL/Supabase en configuration Django."""
    parsed = urlparse(database_url)
    if parsed.scheme not in {'postgres', 'postgresql'}:
        raise ValueError('DATABASE_URL doit commencer par postgres:// ou postgresql://')

    config = {
        'ENGINE': 'django.db.backends.postgresql',
        'NAME': (parsed.path or '/postgres').lstrip('/') or 'postgres',
        'USER': unquote(parsed.username or ''),
        'PASSWORD': unquote(parsed.password or ''),
        'HOST': parsed.hostname or '',
        'PORT': str(parsed.port or 5432),
        'OPTIONS': {
            'sslmode': os.getenv('DATABASE_SSLMODE', 'require'),
        },
        # Important avec Supabase/Supavisor : éviter les connexions persistantes
        # en développement et avec le pooler transactionnel.
        'CONN_MAX_AGE': int(os.getenv('DATABASE_CONN_MAX_AGE', '0')),
    }
    return config


